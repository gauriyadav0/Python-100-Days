# Iterating over a string
name = "Yash"
for i in name:
  print(i)
  if(i=="a"):
    print("This is something special!")

# Iterating over a list
colors = ["Red, Blue, Green, Yellow"]
for color in colors:
  print(color)
  for i in color:
    print(i)

# Range function
for d in range(5):
  print(d+1)

for e in range(1, 7):
  print(e)

for k in range(1,15,9):
  print(k)
  